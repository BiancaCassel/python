def converter_minutos_para_segundos():
    # Ler a quantidade de minutos
    minutos = int(input("Digite a quantidade de minutos: "))
    
    # Calcular o tempo em segundos
    segundos = minutos * 60
    
    # Escrever o resultado na tela
    print("O tempo equivalente em segundos é:", segundos)

# Chamar a função para testar
converter_minutos_para_segundos()

